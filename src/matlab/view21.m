red = ome_readPixelsPlane(2,3,1,0);
bg = ome_readPixelsPlane(2,3,0,0);
redmin = min(red(:));
redmax = max(red(:));
bgmin = min(bg(:));
bgmax = max(bg(:));
pixmin = min([redmin bgmin]);
pixmax = max([redmax bgmax]);
rednorm = (red-pixmin) ./ (pixmax-pixmin);
bgnorm = (bg-pixmin) ./ (pixmax-pixmin);
rednorm1 = rednorm';
bgnorm1 = bgnorm';
imagenorm(:,:,1) = rednorm(:,:);
imagenorm(:,:,2) = bgnorm(:,:);
imagenorm(:,:,3) = 0;
imagenorm1(:,:,1) = rednorm1(:,:);
imagenorm1(:,:,2) = bgnorm1(:,:);
imagenorm1(:,:,3) = 0;
