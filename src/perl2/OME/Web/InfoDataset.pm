sub getPageBody{
  my $self=shift;
  my $cgi=$self->CGI();
  my $body="";
  my $session=$self->Session();  
  my $usergpID=$cgi->url_param('UsergpID');
  if ( !defined $usergpID){
    die "Package needs UsergpID as url parameters. Neither was supplied. This message generated by ".(ref $self)."->createOMEPage().";	
  }
  if (defined $usergpID){
    my @datasets=$session->Factory()->findObjects("OME::Dataset",'group_id'=>$usergpID);

   # my @datasets=OME::Dataset->search( group_id => $usergpID);
    $body.=$cgi->h3("Description");
    $body.=format_popup();
    foreach (@datasets){
	$body.=format_dataset($_);
    }
    $body.=close_button();

  }
   
  return('HTML',$body);




}

