sub format_dataset{
  my ($dataset)=@_;
  my $summary="";
  $summary .= "<NOBR><B>Name:</B> ".$dataset->name()."</NOBR><BR>" ;
  $summary .= "<NOBR><B>ID:</B> ".$dataset->dataset_id()."</NOBR><BR>" ;
  $summary .= "<B>Description:</B> ".$dataset->description()."<BR>" ;
  $summary .= "<NOBR><B>Locked:</B> ".($dataset->locked()?'YES':'NO')."</NOBR><br>";
  $summary .= "<NOBR><B>Owner:</B> ".$dataset->owner()->firstname()." ".$dataset->owner()->lastname()."</NOBR><BR>";
  $summary .="<NOBR><B>E-mail:</B><a href='mailto:".$dataset->owner()->email()."'>".$dataset->owner()->email()."</a></NOBR><BR>";
  $summary .="<NOBR><B>Nb Images in dataset:</B> ".$dataset->images()."</NOBR><BR>";
  $summary.=create_button($dataset->dataset_id());
  $summary .="<br><br>";

  return $summary;

}


sub create_button{
 my ($id)=@_;
 my $text="";
 $text.=<<END;
	<input type=button
	onclick="return OpenPopUpData($id)"
	value="View"
	name="submit">
END
 return $text;
}





sub close_button{
 my $text="";
 $text.=<<END;
	<input type=button
	onclick="window.close()"
	value="Close window">
END
 return $text;
}

